#pragma once

////////////////////////////////////////////////////////////////////////////////////////////////////

#include <libutl/Integer.h>

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_NS_BEGIN;

////////////////////////////////////////////////////////////////////////////////////////////////////

template <class T>
class Integer;

////////////////////////////////////////////////////////////////////////////////////////////////////

/**
   64-bit unsigned integer.

   \author Adam McKee
   \ingroup math
*/

////////////////////////////////////////////////////////////////////////////////////////////////////

class Uint : public Integer<uint64_t>
{
    UTL_CLASS_DECL(Uint, Integer<uint64_t>);
    UTL_CLASS_DEFID;

public:
    /**
       Constructor
       \param i initial value
    */
    Uint(uint64_t i)
        : Integer<uint64_t>(i)
    {
    }

    /**
       Constructor.
       \param str string representation of initial value
    */
    Uint(const String& str)
    {
        set(str);
    }

    void serialize(Stream& stream, uint_t io, uint_t mode = ser_default);
};

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_NS_END;
