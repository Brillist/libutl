#include <libutl/libutl.h>
#include <libutl/BitArrayElem.h>

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_CLASS_IMPL(utl::BitArrayElem);

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_NS_BEGIN;

////////////////////////////////////////////////////////////////////////////////////////////////////

void
BitArrayElem::copy(const Object& rhs)
{
    auto& el = utl::cast<BitArrayElem>(rhs);
    _bitArray = el._bitArray;
    _idx = el._idx;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

String
BitArrayElem::toString() const
{
    return Uint(_bitArray->get(_idx)).toString();
}

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_NS_END;
