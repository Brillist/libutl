#pragma once

////////////////////////////////////////////////////////////////////////////////////////////////////

#include <libutl/HashtableIt.h>

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_NS_BEGIN;

////////////////////////////////////////////////////////////////////////////////////////////////////

/**
   Template version of HashtableIt.

   \author Adam McKee
   \ingroup collection
*/

////////////////////////////////////////////////////////////////////////////////////////////////////

template <class T> class THashtableIt : public HashtableIt
{
    UTL_CLASS_DECL_TPL(THashtableIt, T, HashtableIt);
    UTL_CLASS_DEFID;

public:
    T*
    get() const
    {
        return utl::cast<T>(HashtableIt::get());
    }

    T* operator*() const
    {
        return get();
    }

public:
    // for STL
    typedef T* value_type;
    typedef T*& reference;
    typedef T** pointer;
    typedef std::bidirectional_iterator_tag iterator_category;
    typedef std::ptrdiff_t difference_type;
};

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_NS_END;

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_CLASS_IMPL_TPL(utl::THashtableIt, T);
