/**
   Sentinel maximum value.

   This class finds an instance of itself to be greater-than an instance of any other class.

   \author Adam McKee
   \ingroup general
*/

////////////////////////////////////////////////////////////////////////////////////////////////////

class MaxObject : public Object
{
    UTL_CLASS_DECL(MaxObject, Object);
    UTL_CLASS_DEFID;

public:
    virtual int compare(const Object& rhs) const;
};

////////////////////////////////////////////////////////////////////////////////////////////////////

/**
   Global instance of MaxObject.
   \ingroup general
*/
extern const MaxObject maxObject;

