#include <libutl/libutl.h>
#include <libutl/Vertex.h>

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_CLASS_IMPL(utl::Vertex);

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_NS_BEGIN;

////////////////////////////////////////////////////////////////////////////////////////////////////

void
Vertex::copy(const Object& rhs)
{
    auto& vtx = utl::cast<Vertex>(rhs);
    _object = vtx._object;
    slist_clear(_edgesHead, &_edgesTail, false);
    slist_clone(_edgesHead, &_edgesTail, vtx._edgesHead, false);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_NS_END;
